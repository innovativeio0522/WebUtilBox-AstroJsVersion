        function generateArt() {
            const text = document.getElementById('textInput').value.toUpperCase();
            if (!text) return showToast('Please enter text', true);
            
            const fontStyle = document.getElementById('fontStyle').value;
            const font = fonts[fontStyle];
            
            if (!font) return showToast('Font not found', true);
            
            const height = font['A'].length;
            const lines = Array(height).fill('');
            
            for (const char of text) {
                const charArt = font[char] || font[' '];
                for (let i = 0; i < height; i++) {
                    lines[i] += charArt[i] + ' ';
                }
            }
            
            const result = lines.join('\n');
            document.getElementById('artText').textContent = result;
            document.getElementById('artOutput').classList.add('show');
            showToast('ASCII art generated!');
        }

        function copyArt() {
            const text = document.getElementById('artText').textContent;
            if (!text) return showToast('Nothing to copy', true);
            navigator.clipboard.writeText(text).then(() => showToast('Copied!'));
        }

        function downloadArt() {
            const text = document.getElementById('artText').textContent;
            if (!text) return showToast('Nothing to download', true);
            
            const blob = new Blob([text], { type: 'text/plain' });
            const url = URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.href = url;
            a.download = 'ascii-art.txt';
            a.click();
            URL.revokeObjectURL(url);
            showToast('Downloaded!');
        }

        function clearArt() {
            document.getElementById('textInput').value = '';
            document.getElementById('artText').textContent = '';
            document.getElementById('artOutput').classList.remove('show');
            showToast('Cleared');
        }

        function showToast(message, isError = false) {
            const toast = document.getElementById('toast');
            const msg = document.getElementById('toastMessage');
            msg.textContent = message;
            toast.className = 'toast show' + (isError ? ' error' : '');
            setTimeout(() => toast.classList.remove('show'), 3000);
        }

        // Auto-generate on input
        requestIdleCallback(() => {
            document.getElementById('textInput').addEventListener('input', generateArt);
        }, { timeout: 2000 });